									<h2>Programmes Langue & Sport</h2>
									<br><br>
									10 heures de cours de langue individuels par semaine associés (sauf mention contraire) à 3 sessions individuelles de sport par semaine, d'une durée d'heure environ (si les sessions sont effectuées dans un groupe d'un niveau équivalent au vôtre, elles peuvent durer plus longtemps). Les sessions de sport son généralement dispensées par un professionnel, mais peuvent également être assurées par un membre de la famille d'accueil (selon la disponibilité) ou par l'organisateur local.<br />
Important:<br />
- N'oubliez pas d'indiquer votre niveau sportif sur le formulaire d'inscription.<br />
- Vous êtes tenu(e) de souscrire un contrat d'assurance vous protégeant en cas de maladie ou d'accident, dont vous ou un tiers seriez victimes.<br />
<br />
Cours de langue supplémentaires : £22, 40 € ou 40 $US par heure<br />
Activités de sport supplémentaires sur demande.<br />
<br />
Le programme comprend :<br />
• Hébergement chez votre professeur expérimenté<br />
• 10 heures de cours de langue individuels par semaine<br />
• Pension complète<br />
• 3 sessions individuelles de sport par semaine d'une durée d'une heure environ (sauf mention contraire)<br />
• Tous les frais (location d'équipement, transport, forfaits de ski etc.) <br />
• Séjour du dimanche au dimanche<br>
									
								
															
									<h2>Programmes Langue & Culture</h2>
