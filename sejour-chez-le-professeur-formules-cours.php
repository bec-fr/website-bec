<br />
Cours de langue supplémentaires : £22, 40 € ou 40 $US par heure<br />
Activités culturelles supplémentaires sur demande.<br />
<br />
Le programme comprend :<br />
• Hébergement chez votre professeur expérimenté<br />
• 10 heures de cours de langue individuels par semaine<br />
• La pension complète<br />
• Les activités culturelles mentionnées<br />
