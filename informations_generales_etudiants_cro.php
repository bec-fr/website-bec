Nos écoles partenaires sont situées dans des villes de grande ou moyenne taille, en Europe, aux Etats-Unis et sur des destinations plus lointaines. Cet éventail de destinations, de par sa diversité, pourra satisfaire les aspirations et intérêts culturels de tout un chacun.</p>
                                  <p><strong class="texteBleu">La sélection des écoles partenaires</strong><br />
                                    Nos écoles partenaires sont toutes agréées par des organisations officielles comme le British Council, IALC ou RELSA, qui effectuent des contrôles réguliers de la qualité de l'enseignement dispensé. Elles proposent une gamme complète de cours en petits groupes, émanant de diverses nationalités, ou individuels permettant un apprentissage performant de l'Anglais. Le premier jour, un test de niveau répartit les étudiants en groupes homogènes.
L'encadrement est convivial et disponible, autant pendant les heures passées à l'école que pour vous proposer des activités et sorties  pour découvrir votre ville de séjour.
Le corps enseignant est natif anglophone, diplômé et expérimenté dans l'enseignement de leur langue maternelle à des étudiants étrangers. Les cours sont basés sur des méthodes dynamiques, et l'atmosphère chaleureuse est dynamisée par l'arrivée hebdomadaire de nouveaux étudiants.</p>
                                  <p><strong class="texteBleu">Le voyage</strong><br />
                                    Avec la recrudescence des compagnies low-costs et des tarifs jeunes sur Internet, nous laissons les étudiants réserver eux-mêmes leur transport jusqu'à la ville de destination. Notre service réservation est à votre disposition pour vous donner quelques conseils pour réserver votre voyage et peut réserver pour vous des transferts à votre arrivée et/ou pour votre départ.</p>
                                  <p><strong class="texteBleu">Documents de voyage et visas</strong><br />
                                    Les informations suivantes sont valables pour les étudiants ressortissants de l'Union Européenne. Les personnes ayant une nationalité autre doivent s'enquérir des formalités à accomplir auprès des services consulaires en France du pays de destination. Les démarches et frais d'obtention des visas sont à la charge des participants</p>
                                  <ul type="disc">
                                    <li><strong>Pour un séjour dans l'Union Européenne :</strong> un passeport ou une carte d'identité en cours de validité est requis(e). (A noter que pour un mineur voyageant avec une carte d'identité, une autorisation de sortie du territoire est exigée). Par ailleurs, quelque soit le programme choisit, aucun visa n'est requis.</li>
                                    <li><strong>Pour un séjour aux Etats-Unis :</strong> Un passeport à lecture optique émis au plus tard le 24 octobre 2005 ou un passeport électronique ou biométrique est exigé pour entrer aux USA. Par ailleurs, un formulaire ESTA doit être rempli en ligne afin de demander avant le départ l'autorisation d'entrée sur le territoire américain.</li>
                                  </ul>
                                  
