									<h2>LE VOYAGE</h2><br> <br>   
										
                                 							  
								   
<p>Le voyage est inclus pour tous nos séjours. Nous n'avons retenu au départ de Paris que des voyages <b>en Eurostar ou en avion.</b> Les participants sont pris en charge à Paris à la gare ou à l'aéroport de départ par le moniteur qui les accompagne à l’aller et au retour.</p>

<h3>Départs de province</h3><br>
<p>Pour nos séjours en Angleterre, nous proposons, pour la majorité des séjours, des départs de Lyon et Marseille en TGV avec un accompagnateur. <br>
</p>
<br>
<h3>L’accueil en gare ou à l’aéroport à Paris</h3><br>
<p>S'il doit voyager seul jusqu'à Paris, un représentant de BEC peut accueillir votre enfant à son arrivée dans une gare ou un aéroport parisiens. Un moniteur l'attend devant sa voiture à la gare et le récupère à sa place dans le train (ou après la collecte des bagages à l’aéroport). Pour le retour, le moniteur le dépose à sa place dans son train et attend le départ du train (ou l’assiste pour l’enregistrement des bagages et l’accompagne jusqu’aux contrôles de sécurité à l’aéroport). Lorsque vous souscrivez cette option, vous devez nous faire parvenir la copie du billet de train (ou d’avion) de votre enfant dès que possible et au plus tard, 35 jours avant le départ.
Le coût de l’accueil à Paris est :<br>
-	de 80 € pour un accueil aller/retour entre la gare du Nord et votre gare d’arrivée/de départ ;<br>
-	de 120 € pour un accueil aller/retour entre une gare et un aéroport parisiens.<br>
Un accueil et transfert d'un aéroport vers un autre aéroport parisien est également possible, sur demande. <br></p>

<h2> LES COURS ET LA MÉTHODE PÉDAGOGIQUE</h2><br><br>
<p>Les cours sont dispensés par des professeurs diplômés et expérimentés dans l’enseignement de leur langue maternelle à des étudiants étrangers. En Angleterre, tous nos partenaires sont accrédités par le British Council. L’effectif par classe est de 15 élèves au maximum, avec différents groupes de niveau, de Débutant à Avancé. Le premier jour, un test de langue permet de faire une répartition en groupes homogènes. Les cours, tout en restant ludiques et interactifs, ont pour objectif l'amélioration et le renforcement des quatre compétences linguistiques : expression et compréhension écrites mais aussi et surtout expression et compréhension orales.
Les jeunes reçoivent en fin de séjour, un certificat de stage attestant de leur niveau et de leur assiduité. </p>

<h2>L’HÉBERGEMENT EN FAMILLE</h2><br><br>
<h3>Des conditions préalables</h3><br>
<p>Les responsables régionaux établis dans nos principaux centres de séjour à l’étranger ont pour tâche de choisir les familles qui recevront les enfants.
Grâce à leurs contacts locaux, ils sont en mesure de sélectionner les familles qui sont les mieux à même d’héberger un jeune Français durant quelques semaines chaque année. De nombreux éléments sont pris en compte dans la décision du choix de la famille d’accueil : le niveau moral de la cellule familiale, la qualité de l’hébergement, la disponibilité, la composition de la famille, la distance au lieu de cours.</p>
<h3>Un contrôle systématique</h3><br>
<p>Une fois ces conditions remplies, chaque famille doit encore être en mesure de prouver qu’elle permettra au jeune Français de s’épanouir et de s’imprégner de la langue et de la culture du pays. Cela signifie que votre enfant doit avoir la possibilité de s’intégrer à ce nouveau milieu comme s’il faisait partie de la famille. Tous les repas sont servis selon les traditions du pays : petit déjeuner, déjeuner (ou panier-repas) et dîner.</p> 
<h3>Déplacements</h3><br>
<p>Les participants pourront être amenés à utiliser seuls les transports en commun (sauf pour les séjours 12/14 ans) pour se rendre de leur famille au centre de cours et pour rentrer en fin d’après-midi. Dans ce cas, le trajet n’excède pas 30 à 45 minutes pour les trajets les plus longs. Lors du premier trajet, les jeunes sont accompagnés du moniteur BEC ou de leur famille à des fins de repérage. Les étudiants sont, en général, logés dans les mêmes quartiers et peuvent ainsi faire ces parcours à plusieurs et parfois accompagnés du moniteur BEC. Les plus jeunes sont placés plus proches du centre de cours que leurs aînés. Apprendre à se déplacer seul ou en petits groupes pour un adolescent de plus de 14 ans participe au développement de son autonomie, à la découverte de sa ville de séjour ainsi qu’à l’acquisition d’un vocabulaire nécessaire au quotidien (vérifier son arrêt auprès du chauffeur de bus, lire et comprendre les panneaux de signalisation…). La carte de transport, lorsqu'elle est nécessaire, est toujours incluse dans notre prestation.</p>

<h2>L'HÉBERGEMENT COLLECTIF </h2><br><br>
<p>Nos hébergements collectifs (en campus, en college, à l'hôtel…) sont sélectionnés sur les critères suivants : structures d’accueil, sécurité de l’environnement, confort de l’hébergement, qualité des repas, installations sportives…<br>
Ces structures peuvent être légèrement à l’extérieur de villes, et permettent ainsi de réunir en un seul lieu, cours, hébergement et activités sportives. 
Nos centres accueillent un nombre limité de francophones permettant ainsi de favoriser les échanges en anglais avec des jeunes venus du monde entier présents sur le site. L’hébergement est généralement proposé en chambres individuelles ou doubles, en particulier sur les campus universitaires, mais aussi en chambres triples ou quadruples pour les séjours en hôtels (voir le descriptif de chaque séjour). Tous sont équipés d’infrastructures collectives modernes et fonctionnelles : restaurants universitaires ou réfectoires, terrains de sports, piscine (pour certains), salles communes, grands espaces extérieurs…. Les trois repas sont servis chauds et offrent variété et équilibre. Les jours d’excursion, un panier repas est prévu pour le déjeuner.<br></p>

<h2>L’ENCADREMENT ET LE PROGRAMME D’ACTIVITÉS </h2><br><br>
<p>Dans tous nos centres, notre "petit groupe" de Français rejoint d’autres jeunes venus du monde entier : l’échange linguistique et culturel opère donc, non seulement, durant le temps de cours mais aussi pendant les repas, activités et soirées. Tous nos séjours sont encadrés par des professeurs ou des moniteurs diplômés expérimentés. Le ratio d’encadrement respecte la législation en vigueur. Nous vous donnons le programme prévisionnel de nos séjours dans le descriptif de chacun d'entre eux. Nous nous engageons sur son contenu, en particulier sur les sites et musées dont l'entrée est payante. Cette brochure étant imprimée en janvier 2017, l'ordre des visites peut changer. Par ailleurs, s'il advenait pendant le séjour qu'une visite annoncée ne pouvait être réalisée pour des raisons indépendante de notre volonté, nous nous engageons à la remplacer par la visite d'un site dont l'intérêt et le prix est équivalent, ou à rembourser la différence.<br>
Le coût de toutes nos activités est inclus dans le prix du séjour. <a href="formules-sejours-linguistiques-pour-adolescents.php">Retrouvez sur cette page le détail de nos différentes formules</a>.</p>

                                  
                                
                                 

