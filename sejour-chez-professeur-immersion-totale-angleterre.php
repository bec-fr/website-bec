						<h2>Les cours de langues</h2>
Choisissez 10, 15, 20, 25 ou 30 heures de cours individuels avec un professeur expérimenté. Nous proposons des cours personnalisés en fonction de vos besoins.
 Nous acceptons <b>des étudiants de tout âge</b> (5% de réduction pour les plus de 50 ans) <b>et de tout niveau, du débutant au confirmé</b>
						<h2>Enfants & Adolescents</h2>
Les étudiants ne peuvent se cacher au fond de la classe ou manquer des cours car ils vivent et étudient au domicile de leur professeur. Des enfants de tous âges (à partir de 12 ans)
peuvent être supervisés par le professeur en fonction des souhaits de leurs parents.
						<h2>Adultes & Professionnels</h2>
Les adultes et les professionnels ne se sentiront plus mal à l’aise dans une classe regroupant des étudiantsaux âges et capacités différents. Certains des professeurs
ont des connaissances spécialisées dans desdomaines tels que la médecine, le droit, le marketing, l’ingénierie, la banque, etc. Ils proposent, sur demande,l’option Langue des affaires et vocabulaire spécialisé.
						<h2>Nos Professeurs</h2>
Tous les professeurs sont titulaires d’un diplôme universitaire (ou équivalent) et/ou d’un diplôme d’enseignement reconnu. Ils ont tous reçu la visite d’un des organisateurs
locaux, qui a vérifié leurs diplômes et leurs hébergements.
