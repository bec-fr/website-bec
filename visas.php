									 <h2>Australie</h2>
<b>Pour de courts séjours jusqu'à 12 semaines</b><br>
Un passeport personnel, valable six mois après la fin du séjour, et un visa touristique, appelé eVisitor / ETA (Electronic Travel Authority), suffisent pour partir en Australie
<br>
<b>Visa Vacances-travail</b><br>
Si vous souhaitez participer au programme "working holiday", il vous faudra obtenir un visa spécifique.<br>
Allez à l’ambassade ou au consulat Australien le plus proche de chez vous ou visitez le site internet du gouvernement Australien http://www.immi.gov.au/visitors/working-holiday/visa-options.htm
<br>
<b>Pour des séjours au délà de 12 semaines</b>
Le visa étudiant (Student Visa) vous autorise à étudier et à travailler en Australie pour une durée correspondant à la durée de vos cours plus un mois supplémentaire (28 jours après votre dernier jour de classe). <br>
Le visa étudiant est compatible avec :<br>

<li>Les cours de langue (ex: General English, IELTS Preparation et Cambridge, etc..);
<li>Les cours universitaires (Bachelor degree, Master’s and PhD).
<p>Le visa étudiant peut être approuvé seulement s’il est associé à l’un des cours ci-dessus et s’il est délivré par une école australienne. Pour demander le visa vous aurez besoin d' un numéro COE (Confirmation Of Enrolment) que le BEC vous fournira.<br>
Vous pouvez combiner plusieurs cours avec le même visa, comme des cours de langue et des cours professionnels.<br>
La durée minimum des cours pour souscrire au visa étudiant est de 12 semaines. Le maximum est de 50 semaines.</p>
<br>
<b>Comment faire une demande de visa étudiant ?</b><br>
La demande de visa peut être faite via le site internet du département Australien de l’immigration (www.immi.gov.au). Le temps d’attente est généralement court. Vous devez fournir votre numéro de COE pour démarrer la demande de visa.									 
