                                  <p>Les programmes de stages professionnels non remunérés et volontariat proposés sont conçus pour répondre aux besoins des étudiants de tous âges et horizons confondus. Nous associons les objectifs de carrière de chaque participant au programme, aux compétences des <b>5000 entreprises partenaires spécialisées</b> dans les secteurs d'activités.<br /><br />

Nos programmes de stages vous offrent des opportunités uniques d'étendre vos objectifs personnels et vos objectifs de carrière à travers une expérience professionnelle dans le secteur d'activité qui vous correspond le mieux. Il vous permettra d'acquérir les méthodes nécessaires pour améliorer votre niveau d'Anglais dans un environnement professionnel, d'accroître vos opportunités d'emploi sur un marché de plus en plus compétitif et de vous sensibiliser davantage au monde de l'entreprise et aux méthodes de travail à l'étranger. <br />
Nous nous engageons à trouver des stages qui correspondent aux objectifs professionnels de chaque étudiant, principalement dans les secteurs suivants : <b>hotellerie/restauration, tourisme, voyage, commerce/marketing, comptabilité, finance, secrétariat/administration, droit, ingénierie, gestion d'évènements, mode, informatique/IT, humanitaire, immobilier...</b> <br />Il n'est pas impératif d'avoir déjà acquis une expérience professionnelle dans ces domaines d'activités; toutefois celà constituerait un avantage.<br>Il est impératif d'avoir au moins un niveau d'anglais intermédiaire (B1).
La durée des stages varie de 3 à plusieurs semaines, une longue expérience étant d'autant plus bénéfique.<br /><br />Nous vous proposons pour vos stages à l'étranger les destinations suivantes : <b>Angleterre, Irlande, Etats-Unis, Australie, Nouvelle-Zélande </b> 
