                                  <p class="titreRouge"><strong>L&rsquo;HEBERGEMENT</strong></p>
                                  <p class="texteBleu"><strong>1/La sélection des familles hôtesses  </strong></p>
<p>Les élèves et les professeurs sont accueillis dans des familles qui ont été soigneusement sélectionnées par nos directions locales.<br />
Grâce à leurs contacts locaux, les correspondants régionaux du BEC établis en permanence dans nos principaux centres de séjours à l’étranger, ont pour tâche de choisir les familles qui recevront les élèves. De nombreux éléments sont pris en compte dans le choix des familles d’accueil : le niveau moral de la famille, la qualité de l’hébergement, la nourriture, la disponibilité des membres de la famille et la capacité à permettre à l’élève de s’imprégner de la culture du pays.<br />
Les élèves sont généralement hébergés à 2 ou 3 par famille. A certaines périodes, notamment en Avril ou  pendant les périodes de fêtes locales (notamment la Semaine Sainte en Espagne, Les Fallas à Valence et la Féria de Séville), les familles sont très sollicitées et peuvent accueillir 4 élèves.
</p>
<p class="texteBleu"><strong>2/La vie en famille</strong></p>
                                  <p>Le premier jour, les familles hôtesses accueillent les élèves, pour la première nuit, au point de rendez-vous avec leurs bagages. Pour les jours suivants, les familles indiquent aux élèves le chemin pour rejoindre le point de ramassage, distant d’environ 10 à 15 minutes à pieds. Sur certains centres, les familles déposent et récupèrent tous les jours les élèves au point de rendez-vous. C’est à cet endroit que l’autocar effectue un ramassage le matin et dépose les élèves le soir. Certains centres prévoient, pour des raisons pratiques, 2 points de ramassage.<br />
Les familles offrent la pension complète du premier dîner, le soir de l’arrivée, au panier repas du déjeuner du dernier jour : les petits déjeuners et dîners sont pris en famille. Les déjeuners sont fournis sous forme de paniers repas, donnant au groupe plus de souplesse pour ses déplacements. Les soirées se passent généralement en famille sauf si le programme prévoit une activité spécifique.
</p>
<p class="texteBleu"><strong>3/H&eacute;bergement en Auberges ou h&ocirc;tels</strong></p>
<p>Sur certaines destinations, nous offrons également un hébergement en résidence, en auberge ou en hôtel. Ces centres conviennent particulièrement à l’accueil de jeunes. L’hébergement s’effectue généralement en chambres multiples pour les élèves et en chambres doubles pour les professeurs, avec toilettes et douches (à l’étage ou en chambre), salles de séjour, télévision. Logez au cœur de Londres, Barcelone, New-York, Berlin et de bien d’autres villes encore. N’hésitez pas à nous adresser votre demande.</p>
