                                  <p class="titreRouge"><strong>LA REGLEMENTATION APPLICABLE AU TRANSPORT ROUTIER DE PERSONNES </strong></p>

<p class="texteBleu"><strong>VEHICULE</strong></p>
<p>Tout véhicule de transport en commun subit tous les 6 mois une visite technique effectuée par le Service des Mines. Ce service établit la Carte Violette de l’Autocar qui fixe notamment le nombre de places et fait apparaître la validité du contrôle technique. Les passagers doivent être transportés assis.</p>

<p class="texteBleu"><strong>REGLEMENTATION POUR UN SEUL CONDUCTEUR</strong></p>
<p>Durée de conduite continue : un conducteur seul ne peut conduire plus de 4h30 sans interruption le jour et 4 h la nuit (de 22h à 7h). Il doit bénéficier d’une pause au moins égale à 45 minutes à l’issue de ce temps de conduite ou de deux pauses successives de 15  puis 30 minutes réparties au cours de la période.</p>

<p>Durée de conduite journalière : le temps de conduite journalière est limité à 9 heures ; prolongeable jusqu’à 10 heures, deux fois par semaine.</p>

<p>Amplitude : c’est la durée séparant la prise de service de la fin de service, lors d’une journée de travail d’un conducteur. Cette amplitude ne peut excéder 14 heures. A noter que le conducteur a commencé son service avant de vous prendre en charge, et qu’il l’achèvera après vous avoir déposé au retour. Si l’amplitude du parcours prévu entraîne une amplitude supérieure, la mise à disposition par le transporteur d’un chauffeur supplémentaire doit être prévue, sauf si le voyage est interrompu.</p>

<p>Temps de repos : le temps de repos quotidien est de 11 heures consécutives, mais peut être ramené à 9 heures, 3 fois par semaine.
Depuis le 1er juin 2010, un décret permet dans le cadre d’un voyage international à un chauffeur, à certaines conditions, d’effectuer 12 journées consécutives de travail. </p>

<p class="texteBleu"><strong>CONDUCTEUR EN DOUBLE EQUIPAGE</strong></p>

<p>La loi impose une amplitude maximale de 18 heures en double équipage. Au delà, il faut interrompre le voyage ou prévoir un troisième chauffeur. Le décompte de l’amplitude est interrompu si le ou les chauffeurs bénéficient d’une pause continue de 9 heures (couchette à bord d’un ferry ou véhicule à l’arrêt).</p>

<p class="texteBleu"><strong>CEINTURES DE SECURITE</strong></p>

<p>Le décret n° 2003-637 du 9 juillet 2003 étend l’obligation du port de la ceinture de sécurité aux occupants des véhicules de transport en commun de personnes lorsque les sièges sont équipés d’une ceinture de sécurité. Seuls les autocars mis en circulation après le 1er octobre 1999 sont concernés, et ceux qui ont été équipés par construction avant l’échéance précitée.  En cas de défaut de port par les passagers, la responsabilité pénale reste individuelle pour chacun d’entre eux dès lors qu’ils sont âgés de 13 ans et plus. En aucun cas la responsabilité du conducteur et de son employeur ne pourra être mise en cause.</p>


<p class="texteBleu"><strong>LISTE DE PASSAGERS</strong></p>
<p>Conformément aux dispositions de l’arrêté ministériel du 3 juillet 2009, une liste nominative des passagers embarqués devra obligatoirement se trouver à bord du véhicule pour tout service collectif de transport occasionnel hors de la zone constitué par le département de prise en charge et des départements limitrophes. En cas de transport en commun d’enfants, la liste devra en outre comporter les coordonnées téléphoniques d’une personne à contacter pour chaque enfant transporté</p>

